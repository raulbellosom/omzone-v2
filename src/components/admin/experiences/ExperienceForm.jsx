import { useState, useEffect } from "react";
import { Input } from "@/components/common/Input";
import { Button } from "@/components/common/Button";
import { Card } from "@/components/common/Card";
import SlugInput from "./SlugInput";
import ImageUpload from "./ImageUpload";
import GalleryManager from "@/components/admin/media/GalleryManager";
import { slugify, checkSlugAvailable } from "@/hooks/useExperiences";
import AdminSelect from "@/components/common/AdminSelect";
import { useLanguage } from "@/hooks/useLanguage";
import { cn } from "@/lib/utils";

const TYPE_OPTIONS = [
  { value: "session", i18nKey: "admin.experienceTypes.session" },
  { value: "immersion", i18nKey: "admin.experienceTypes.immersion" },
  { value: "retreat", i18nKey: "admin.experienceTypes.retreat" },
  { value: "stay", i18nKey: "admin.experienceTypes.stay" },
  { value: "private", i18nKey: "admin.experienceTypes.private" },
  { value: "package", i18nKey: "admin.experienceTypes.package" },
];
const SALE_MODE_OPTIONS = [
  { value: "direct", i18nKey: "admin.saleModes.direct" },
  { value: "request", i18nKey: "admin.saleModes.request" },
  { value: "assisted", i18nKey: "admin.saleModes.assisted" },
  { value: "pass", i18nKey: "admin.saleModes.pass" },
];
const FULFILLMENT_OPTIONS = [
  { value: "ticket", i18nKey: "admin.fulfillmentTypes.ticket" },
  { value: "booking", i18nKey: "admin.fulfillmentTypes.booking" },
  { value: "pass", i18nKey: "admin.fulfillmentTypes.pass" },
  { value: "package", i18nKey: "admin.fulfillmentTypes.package" },
];
const STATUS_OPTIONS = [
  { value: "draft", i18nKey: "admin.statuses.draft" },
  { value: "published", i18nKey: "admin.statuses.published" },
  { value: "archived", i18nKey: "admin.statuses.archived" },
];

const EMPTY = {
  name: "",
  publicName: "",
  publicNameEs: "",
  slug: "",
  type: "session",
  saleMode: "direct",
  fulfillmentType: "ticket",
  shortDescription: "",
  shortDescriptionEs: "",
  longDescription: "",
  longDescriptionEs: "",
  heroImageId: "",
  galleryImageIds: [],
  requiresSchedule: false,
  requiresDate: false,
  allowQuantity: false,
  maxQuantity: "",
  minQuantity: "",
  generatesTickets: true,
  status: "draft",
  sortOrder: "",
  seoTitle: "",
  seoDescription: "",
};

function Field({ label, required, error, hint, children }) {
  return (
    <div className="space-y-1.5">
      <label className="text-sm font-medium text-charcoal">
        {label}
        {required && <span className="text-red-500 ml-0.5">*</span>}
      </label>
      {children}
      {error && <p className="text-xs text-red-600">{error}</p>}
      {hint && !error && <p className="text-xs text-charcoal-subtle">{hint}</p>}
    </div>
  );
}

function Toggle({ checked, onChange, label, disabled }) {
  return (
    <label className="flex items-center gap-3 cursor-pointer select-none">
      <button
        type="button"
        role="switch"
        aria-checked={checked}
        onClick={() => onChange(!checked)}
        disabled={disabled}
        className={cn(
          "relative inline-flex h-6 w-11 shrink-0 rounded-full border-2 border-transparent transition-colors focus:outline-none focus:ring-2 focus:ring-sage/40",
          checked ? "bg-sage" : "bg-sand-dark",
          "disabled:opacity-50 disabled:cursor-not-allowed",
        )}
      >
        <span
          className={cn(
            "pointer-events-none block h-5 w-5 rounded-full bg-white shadow-sm transition-transform",
            checked ? "translate-x-5" : "translate-x-0",
          )}
        />
      </button>
      <span className="text-sm text-charcoal">{label}</span>
    </label>
  );
}

function Textarea({
  value,
  onChange,
  placeholder,
  disabled,
  rows = 3,
  maxLength,
  error,
}) {
  return (
    <textarea
      value={value ?? ""}
      onChange={(e) => onChange(e.target.value)}
      placeholder={placeholder}
      disabled={disabled}
      rows={rows}
      maxLength={maxLength}
      className={cn(
        "flex w-full rounded-xl border border-sand-dark bg-white px-4 py-3 text-sm text-charcoal placeholder:text-charcoal-subtle",
        "focus:outline-none focus:border-sage focus:ring-2 focus:ring-sage/20 resize-none",
        "disabled:opacity-50 disabled:bg-warm-gray",
        error && "border-red-400",
      )}
    />
  );
}

function parseJsonArray(val) {
  if (Array.isArray(val)) return val;
  if (!val) return [];
  try {
    return JSON.parse(val) ?? [];
  } catch {
    return [];
  }
}

export default function ExperienceForm({
  initialData,
  onSubmit,
  submitting,
  submitLabel = "Guardar",
}) {
  const { t } = useLanguage();
  const [form, setForm] = useState(() => ({
    ...EMPTY,
    ...initialData,
    galleryImageIds: parseJsonArray(initialData?.galleryImageIds),
  }));
  const [errors, setErrors] = useState({});
  const [slugAutoGenerated, setSlugAutoGenerated] = useState(
    !initialData?.slug,
  );

  function set(field, value) {
    setForm((prev) => ({ ...prev, [field]: value }));
    if (errors[field])
      setErrors((prev) => {
        const e = { ...prev };
        delete e[field];
        return e;
      });
  }

  // Auto-generate slug from publicName
  function handlePublicName(value) {
    set("publicName", value);
    if (slugAutoGenerated) {
      set("slug", slugify(value));
    }
  }

  function handleSlugChange(value) {
    setSlugAutoGenerated(false);
    set("slug", value);
  }

  async function validate() {
    const e = {};
    if (!form.name.trim()) e.name = "El nombre interno es requerido";
    if (!form.publicName.trim())
      e.publicName = "El nombre público es requerido";
    if (!form.slug.trim()) {
      e.slug = "El slug es requerido";
    } else if (!/^[a-z0-9]+(-[a-z0-9]+)*$/.test(form.slug)) {
      e.slug = "El slug solo puede contener minúsculas, números y guiones";
    } else {
      const available = await checkSlugAvailable(form.slug, initialData?.$id);
      if (!available) e.slug = "Este slug ya está en uso";
    }
    if (!form.type) e.type = "El tipo es requerido";
    if (!form.saleMode) e.saleMode = "El modo de venta es requerido";
    if (!form.fulfillmentType)
      e.fulfillmentType = "El fulfillment es requerido";
    return e;
  }

  async function handleSubmit(e) {
    e.preventDefault();
    const validationErrors = await validate();
    if (Object.keys(validationErrors).length > 0) {
      setErrors(validationErrors);
      return;
    }

    const payload = {
      name: form.name.trim(),
      publicName: form.publicName.trim(),
      publicNameEs: form.publicNameEs.trim() || null,
      slug: form.slug.trim(),
      type: form.type,
      saleMode: form.saleMode,
      fulfillmentType: form.fulfillmentType,
      shortDescription: form.shortDescription.trim() || null,
      shortDescriptionEs: form.shortDescriptionEs.trim() || null,
      longDescription: form.longDescription.trim() || null,
      longDescriptionEs: form.longDescriptionEs.trim() || null,
      heroImageId: form.heroImageId || null,
      galleryImageIds:
        form.galleryImageIds.length > 0
          ? JSON.stringify(form.galleryImageIds)
          : null,
      requiresSchedule: form.requiresSchedule,
      requiresDate: form.requiresDate,
      allowQuantity: form.allowQuantity,
      maxQuantity:
        form.allowQuantity && form.maxQuantity
          ? parseInt(form.maxQuantity)
          : null,
      minQuantity:
        form.allowQuantity && form.minQuantity
          ? parseInt(form.minQuantity)
          : null,
      generatesTickets: form.generatesTickets,
      status: form.status,
      sortOrder: form.sortOrder ? parseInt(form.sortOrder) : null,
      seoTitle: form.seoTitle.trim() || null,
      seoDescription: form.seoDescription.trim() || null,
    };

    await onSubmit(payload);
  }

  const isDisabled = submitting;

  return (
    <form onSubmit={handleSubmit} className="space-y-6">
      {/* Identidad */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          {t("admin.experienceForm.sectionIdentity")}
        </h2>
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
          <Field
            label={t("admin.experienceForm.internalName")}
            required
            error={errors.name}
          >
            <Input
              value={form.name}
              onChange={(e) => set("name", e.target.value)}
              placeholder={t("admin.experienceForm.placeholderInternalName")}
              disabled={isDisabled}
              className={errors.name ? "border-red-400" : ""}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.publicNameEn")}
            required
            error={errors.publicName}
          >
            <Input
              value={form.publicName}
              onChange={(e) => handlePublicName(e.target.value)}
              placeholder={t("admin.experienceForm.placeholderPublicNameEn")}
              disabled={isDisabled}
              className={errors.publicName ? "border-red-400" : ""}
            />
          </Field>
          <Field label={t("admin.experienceForm.publicNameEs")}>
            <Input
              value={form.publicNameEs}
              onChange={(e) => set("publicNameEs", e.target.value)}
              placeholder={t("admin.experienceForm.placeholderPublicNameEs")}
              disabled={isDisabled}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.slug")}
            required
            error={errors.slug}
          >
            <SlugInput
              value={form.slug}
              onChange={handleSlugChange}
              error={errors.slug}
              disabled={isDisabled}
            />
          </Field>
        </div>
      </Card>

      {/* Clasificación */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          {t("admin.experienceForm.sectionClassification")}
        </h2>
        <div className="grid grid-cols-1 sm:grid-cols-3 gap-4">
          <Field
            label={t("admin.experienceForm.type")}
            required
            error={errors.type}
          >
            <AdminSelect
              value={form.type}
              onChange={(v) => set("type", v)}
              options={TYPE_OPTIONS.map((o) => ({ ...o, label: t(o.i18nKey) }))}
              disabled={isDisabled}
              error={errors.type}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.saleMode")}
            required
            error={errors.saleMode}
          >
            <AdminSelect
              value={form.saleMode}
              onChange={(v) => set("saleMode", v)}
              options={SALE_MODE_OPTIONS.map((o) => ({
                ...o,
                label: t(o.i18nKey),
              }))}
              disabled={isDisabled}
              error={errors.saleMode}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.fulfillment")}
            required
            error={errors.fulfillmentType}
          >
            <AdminSelect
              value={form.fulfillmentType}
              onChange={(v) => set("fulfillmentType", v)}
              options={FULFILLMENT_OPTIONS.map((o) => ({
                ...o,
                label: t(o.i18nKey),
              }))}
              disabled={isDisabled}
              error={errors.fulfillmentType}
            />
          </Field>
        </div>
      </Card>

      {/* Descripción */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          {t("admin.experienceForm.sectionDescription")}
        </h2>
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
          <Field
            label={t("admin.experienceForm.shortDescriptionEn")}
            hint={t("admin.experienceForm.maxChars500")}
          >
            <Textarea
              value={form.shortDescription}
              onChange={(v) => set("shortDescription", v)}
              placeholder={t("admin.experienceForm.placeholderShortDescEn")}
              disabled={isDisabled}
              maxLength={500}
            />
          </Field>
          <Field label={t("admin.experienceForm.shortDescriptionEs")}>
            <Textarea
              value={form.shortDescriptionEs}
              onChange={(v) => set("shortDescriptionEs", v)}
              placeholder="Una sesión de yoga transformadora al amanecer..."
              disabled={isDisabled}
              maxLength={500}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.longDescriptionEn")}
            hint={t("admin.experienceForm.maxChars5000")}
          >
            <Textarea
              value={form.longDescription}
              onChange={(v) => set("longDescription", v)}
              placeholder={t("admin.experienceForm.placeholderLongDescEn")}
              disabled={isDisabled}
              rows={5}
              maxLength={5000}
            />
          </Field>
          <Field label={t("admin.experienceForm.longDescriptionEs")}>
            <Textarea
              value={form.longDescriptionEs}
              onChange={(v) => set("longDescriptionEs", v)}
              placeholder={t("admin.experienceForm.placeholderLongDescEs")}
              disabled={isDisabled}
              rows={5}
              maxLength={5000}
            />
          </Field>
        </div>
      </Card>

      {/* Imagen de portada */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          {t("admin.experienceForm.sectionCoverImage")}
        </h2>
        <div className="max-w-lg">
          <ImageUpload
            fileId={form.heroImageId}
            onUpload={(id) => set("heroImageId", id)}
            onRemove={() => set("heroImageId", "")}
            disabled={isDisabled}
          />
        </div>
      </Card>

      {/* Galería de imágenes */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          {t("admin.experienceForm.sectionGallery")}
        </h2>
        <GalleryManager
          value={form.galleryImageIds}
          onChange={(ids) => set("galleryImageIds", ids)}
          isAdmin
          disabled={isDisabled}
        />
      </Card>

      {/* Comportamiento */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          Comportamiento
        </h2>
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          <Toggle
            checked={form.requiresSchedule}
            onChange={(v) => set("requiresSchedule", v)}
            label={t("admin.experienceForm.requiresSlot")}
            disabled={isDisabled}
          />
          <Toggle
            checked={form.requiresDate}
            onChange={(v) => set("requiresDate", v)}
            label={t("admin.experienceForm.requiresDate")}
            disabled={isDisabled}
          />
          <Toggle
            checked={form.generatesTickets}
            onChange={(v) => set("generatesTickets", v)}
            label={t("admin.experienceForm.generatesTickets")}
            disabled={isDisabled}
          />
          <Toggle
            checked={form.allowQuantity}
            onChange={(v) => set("allowQuantity", v)}
            label={t("admin.experienceForm.multipleAttendees")}
            disabled={isDisabled}
          />
        </div>
        {form.allowQuantity && (
          <div className="grid grid-cols-2 gap-4 pt-2">
            <Field label={t("admin.experienceForm.minAttendees")}>
              <Input
                type="number"
                min={1}
                value={form.minQuantity}
                onChange={(e) => set("minQuantity", e.target.value)}
                placeholder="1"
                disabled={isDisabled}
              />
            </Field>
            <Field label={t("admin.experienceForm.maxAttendees")}>
              <Input
                type="number"
                min={1}
                value={form.maxQuantity}
                onChange={(e) => set("maxQuantity", e.target.value)}
                placeholder="10"
                disabled={isDisabled}
              />
            </Field>
          </div>
        )}
      </Card>

      {/* Publicación */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          Publicación
        </h2>
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          <Field label={t("admin.experienceForm.status")} required>
            <AdminSelect
              value={form.status}
              onChange={(v) => set("status", v)}
              options={STATUS_OPTIONS.map((o) => ({
                ...o,
                label: t(o.i18nKey),
              }))}
              disabled={isDisabled}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.displayOrder")}
            hint="Número para ordenar en listado público"
          >
            <Input
              type="number"
              min={0}
              value={form.sortOrder}
              onChange={(e) => set("sortOrder", e.target.value)}
              placeholder="0"
              disabled={isDisabled}
            />
          </Field>
        </div>
      </Card>

      {/* SEO */}
      <Card className="p-5 space-y-4">
        <h2 className="text-sm font-semibold text-charcoal-subtle uppercase tracking-wider">
          SEO
        </h2>
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
          <Field
            label={t("admin.experienceForm.seoTitle")}
            hint={t("admin.experienceForm.maxChars500")}
          >
            <Input
              value={form.seoTitle}
              onChange={(e) => set("seoTitle", e.target.value)}
              placeholder="Sunrise Yoga Experience | OMZONE"
              disabled={isDisabled}
              maxLength={255}
            />
          </Field>
          <Field
            label={t("admin.experienceForm.seoDescription")}
            hint={t("admin.experienceForm.maxChars500")}
          >
            <Textarea
              value={form.seoDescription}
              onChange={(v) => set("seoDescription", v)}
              placeholder="Discover our transformative sunrise yoga..."
              disabled={isDisabled}
              maxLength={500}
              rows={2}
            />
          </Field>
        </div>
      </Card>

      {/* Actions */}
      <div className="flex items-center gap-3 pb-6">
        <Button type="submit" disabled={isDisabled} size="md">
          {submitting ? (
            <span className="flex items-center gap-2">
              <span className="w-4 h-4 rounded-full border-2 border-white border-t-transparent animate-spin" />
              Guardando...
            </span>
          ) : (
            submitLabel
          )}
        </Button>
      </div>
    </form>
  );
}
